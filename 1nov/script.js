//              Arrival from BRU to FAO (11h45)
//   üî¥ Delayed Arrival from HEL to FAO (12h05)
//            Departure from FAO to LIS (12h30)

const flightsArr = flights.split('+');

for (const flight of flightsArr) {
  let [first, second, third, fourth] = flight.split(';');
  first = first.replaceAll('_', ' ').slice(1);
  second = second.slice(0, 3);
  third = third.slice(0, 3);
  fourth = fourth.split(':').join('h');
  const sentence = `${
    first.startsWith('Delayed') ? '‚ù§' : ''
  } ${first} from ${second} to ${third} (${fourth})`;

  console.log(sentence.padStart(50, ' '));
}

