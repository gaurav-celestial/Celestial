const button = document.querySelector('button');
const textarea = document.querySelector('textarea');

button.addEventListener('click', function () {
  const arr = textarea.value.toLowerCase().split('\n');
  arr.forEach((a, i) => {
    const trimmed = a.trim().split('_');
    const first = trimmed[0];
    const second = trimmed[1][0].toUpperCase() + trimmed[1].slice(1);
    const final = first + second;
    console.log(final.padEnd(20, ' ') + `${'üòé'}`.repeat(i + 1));
  });
});

